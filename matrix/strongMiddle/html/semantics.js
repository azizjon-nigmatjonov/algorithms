// HTML semantics refers to the proper use of HTML tags to give meaning and structure to the content of a web page.
// The purpose of using HTML semantics is to make the page more accessible to both human users and search engine crawlers,
// allowing them to better understand the content and its context.

// Some common examples of semantic HTML tags include:
// 1. <header>: Used to define the header section of a web page, typically containing branding, navigation menus, and other important information.
// 2. <nav>: Used to define a section of a web page that contains navigation links, typically within the header or footer.
// 3. <main>: Used to define the main content section of a web page, typically excluding the header, footer, and sidebar.
