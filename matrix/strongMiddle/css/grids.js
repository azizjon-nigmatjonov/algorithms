// CSS Grid is a layout module that allows you to create complex two-dimensional grid layouts. With CSS Grid, you can divide a webpage into rows and columns, and then place elements into specific grid cells.
// Here's an example of a simple CSS Grid layout with two rows and three columns:
// .container {
//     display: grid;
//     grid-template-rows: 100px 100px;
//     grid-template-columns: 1fr 1fr 1fr;
//   }
  
//   .item {
//     grid-row: 1 / 2;
//     grid-column: 2 / 3;
//   }
  
// In this example, the container element is set to display as a grid with two rows and three columns. The first row is set to be 100 pixels tall, and the second row is also set to be 100 pixels tall. Each column is set to take up one third of the available space using the 1fr value.

// The item element is placed in the first row and the second column of the grid using the grid-row and grid-column properties. The values 1 / 2 and 2 / 3 specify that the element should span one row and one column.

