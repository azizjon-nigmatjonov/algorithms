// CSS allows you to set colors for various elements on a webpage. There are different ways to specify colors in CSS:
// 1. Color names: CSS supports a variety of predefined color names such as red, blue, green, yellow, etc.
// 2. Hexadecimal values: You can also specify colors using a hexadecimal value. A hexadecimal value is a six-digit code that represents a color. The first two digits represent the amount of red, the second two digits represent the amount of green, and the last two digits represent the amount of blue. For example, #FF0000 is pure red, and #00FF00 is pure green.
// 3. RGB values: RGB stands for red, green, and blue, and is a way to specify colors using numerical values for each color component. For example, rgb(255, 0, 0) is pure red, and rgb(0, 255, 0) is pure green.
// 4. RGBA values: RGBA stands for red, green, blue, and alpha, and is a way to specify colors using numerical values for each color component as well as the alpha value, which represents the transparency of the color. For example, rgba(255, 0, 0, 0.5) is semi-transparent red.

// Here are some examples of how to set colors using CSS:
// .color-name {
//     color: red;
//   }
  
//   .hex-value {
//     color: #00FF00;
//   }
  
//   .rgb-value {
//     color: rgb(0, 0, 255);
//   }
  
//   .rgba-value {
//     color: rgba(255, 0, 0, 0.5);
//   }

