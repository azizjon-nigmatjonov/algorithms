// CSS, or Cascading Style Sheets, is a style sheet language used to describe the presentation of a document written in HTML or XML.
// CSS is used to control the layout, colors, fonts, and other visual elements of a webpage.

// The syntax of CSS is relatively straightforward, consisting of a set of rules that define how certain HTML elements should be styled.
//  Here is an example of a simple CSS rule:

// p {
//     color: red;
//     font-size: 16px;
//  }

// In this example, the CSS rule applies to all `p` elements on the page, and specifies that the text color should be red, and the font size
// should be 16 pixels.

// CSS rules are made up of two main parts: the selector and the declaration. The selector specifies which HTML element or elements
// the rule applies to, and the declaration specifies the style properties and values to be applied.
