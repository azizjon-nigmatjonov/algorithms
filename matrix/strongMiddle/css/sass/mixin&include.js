// 1. @mixin
// A Sass mixin is a block of reusable code that can be included in multiple stylesheets. Mixins are useful for reducing repetition in 
// your code and keeping your styles organized.

// To create a mixin in Sass, you use the @mixin directive followed by a name and a set of curly braces containing the styles you want 
// to include:

// @mixin my-mixin {
//     font-size: 16px;
//     color: #333;
//  }
  
// 2. @include
// To use the mixin in your code, you use the @include directive followed by the name of the mixin:
// .my-class {
//     @include my-mixin;
//   }
  